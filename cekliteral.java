public class cekliteral {
	public static void main(String[] args) {
		long c=41; //tipe data diawali huruf kecil kecuali String
		int i=4;
		double a=4d;
		boolean b=true;
		String nama="UIN Maliki Malang";
		char x='X';
		byte n=10;
		float m=10F;
		
		System.out.println("Nilai interger="+i); // System diawali huruf besar
		System.out.println("Nilai Double="+a);
		System.out.println("Nilai long="+c);
		System.out.println("Nilai Boolean="+b);
		System.out.println("Nilai String="+nama);
		System.out.println("Nilai Char="+x);
		System.out.println("Nilai Byte="+n);
		System.out.println("Nilai Float="+m);
	}
}
